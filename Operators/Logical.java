public class Logical {
    public static void main(String args[]){
        // logical And operator
        System.out.println((3>2) && (5>0) );
        System.out.println((3<2) && (5>1) );

        // Logical OR operator
        System.out.println((3>2) || (5>4));
        System.out.println((3<2) || (5>4));

        // Logical NOT
        System.out.println(!(0>5));
        System.out.println(!(2<8));
    }    
}
